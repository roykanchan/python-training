def addition(num1, num2):
    add = num1 + num2
    return add

def sub(num1, num2):
    s = num1 - num2
    return s

def mul(num1, num2):
    m = num1 * num2
    return m

def div(num1, num2):
    d = num1/num2
    return d

print(addition(10,5))
print(sub(10,5))
print(mul(10,5))
print(div(10,5))

